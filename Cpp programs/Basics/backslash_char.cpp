//Uses of back slashes or  backslash character constants.   
#include<iostream>
using namespace std;
int main(){
    cout<<"hello world\n";          //new line backslash 
    cout<<"ok\n";

    cout<<"hello cpp\t";           //for tab we use backslash t for spacing 5 blocks
    cout<<"OK\n";

    cout<<"hey\a";                 //for beep we use backslah a 
    cout<<"Ok";

    cout<<"\n\"my name is abc\"";           //this way we can print single or double brackets within a string

    return 0;
}